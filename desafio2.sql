SELECT
JOB_TITLE AS 'Cargo',
CASE
WHEN MIN_SALARY >= 5000 AND MIN_SALARY <= 10000 THEN 'Baixo'
WHEN MIN_SALARY >= 10001 AND MIN_SALARY <= 20000 THEN 'Médio'
WHEN MIN_SALARY >= 20001 AND MIN_SALARY <= 30000 THEN 'Alto'
WHEN MIN_SALARY > 30000 THEN 'Altíssimo'
END AS 'Nível'
FROM hr.jobs
ORDER BY JOB_TITLE;
