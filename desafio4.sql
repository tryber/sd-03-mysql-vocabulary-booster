SELECT B.JOB_TITLE AS Cargo,
ROUND(AVG(A.SALARY), 2) AS "Média salarial",
CASE
WHEN ROUND(AVG(A.SALARY), 2) BETWEEN 2000 AND 5800 THEN "Júnior"
WHEN ROUND(AVG(A.SALARY), 2) BETWEEN 5801 AND 7500 THEN "Pleno"
WHEN ROUND(AVG(A.SALARY), 2) BETWEEN 7500 AND 10500 THEN "Sênior"
ELSE "CEO"
END AS Senioridade
FROM hr.employees AS A
JOIN hr.jobs AS B
ON A.JOB_ID = B.JOB_ID
GROUP BY A.JOB_ID
ORDER BY ROUND(AVG(A.SALARY), 2), Cargo;
