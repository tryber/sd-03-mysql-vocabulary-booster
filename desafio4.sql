SELECT j.JOB_TITLE AS 'Cargo', ROUND(AVG(e.SALARY), 2) AS 'MÃ©dia salarial',
CASE
	WHEN e.SALARY BETWEEN 2000 AND 5800 THEN 'Junior'
    WHEN e.SALARY BETWEEN 5801 AND 7500 THEN 'Pleno'
    WHEN e.SALARY BETWEEN 7501 AND 10500 THEN 'Senior'
    WHEN e.SALARY > 10500 THEN 'CEO'
END AS 'Senioridade'
FROM hr.jobs AS j
JOIN hr.employees AS e
ON j.JOB_ID = e.JOB_ID
GROUP BY j.JOB_TITLE;
